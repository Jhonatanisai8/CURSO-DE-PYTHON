import math

PI = 3.14
print("Redondeado la variable Pi: "+str(PI))
print(round(PI))
print("")

print("Redondeado un numero hacia arriba la variable Pi: "+str(PI))
print(math.ceil(PI))
print("")

print("Redondeado un numero hacia abajo la variable Pi: "+str(PI))
print(math.floor(PI))
print("")

print("valor absoluto de la variable Pi: "+str(PI))
print(abs(PI))
print("")

print("Elevar a un exponente la variable Pi: "+str(PI))
print(pow(PI,2))
print("")

print("Raiz cuadrada de 9")
print(math.sqrt(9))
print("")

#metodo para sabe el maximo y minimo
